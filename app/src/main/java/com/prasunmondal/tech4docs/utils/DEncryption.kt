// https://stackoverflow.com/questions/16950833/is-there-an-easy-way-to-encrypt-a-java-object


class DEncryption {
    companion object {
        fun encrypt(input: String, key: String): String {
            return input
        }

        fun decrypt(input: String, key: String): String {
            return input
        }
    }
}